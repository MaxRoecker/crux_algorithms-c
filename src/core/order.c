inline CRUX__Order CRUX__compare_char(const Char a, const Char b) {
  const CRUX__Order order = (!CRUX__is_less(a, b))
    ? (!CRUX__is_greater(a, b)) ? CRUX__EQUAL : CRUX__GREATER : CRUX__LESS;
  return order;
}


