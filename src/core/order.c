CRUX__ResultOrder CRUX__compare_addr_char(
    const void *const one,
    const void *const another) {
  CRUX__Trace trace = CRUX__trace_create();
  CRUX__Order order = CRUX__EQUAL;
  if (is_nil(one, void) || is_nil(another, void)) {
    CRUX__Fault fault = {
      .error = CRUX__ERROR_VALUE,
      .info = "The addresses to be compared are nil.",
      .filepath = __FILE__,
      .line = (((IU32) __LINE__) - CRUX__as_iu32(5))};
    CRUX__trace_push(&trace, fault);
  } else {
    const Char one_value = *((const Char *) one);
    const Char another_value = *((const Char *) another);
    order = CRUX__compare(one_value, another_value);
  }
  CRUX__ResultOrder result = {.trace = trace, .value = order};
  return result;
}


